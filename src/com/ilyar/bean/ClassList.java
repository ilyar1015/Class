package com.ilyar.bean;

public class ClassList {
    public String getClassBuild() {
        return ClassBuild;
    }

    public void setClassBuild(String classBuild) {
        ClassBuild = classBuild;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getClassUsing() {
        return ClassUsing;
    }

    public void setClassUsing(String classUsing) {
        ClassUsing = classUsing;
    }

    public String getClassStart() {
        return ClassStart;
    }

    public void setClassStart(String classStart) {
        ClassStart = classStart;
    }

    public String getClassEnd() {
        return ClassEnd;
    }

    public void setClassEnd(String classEnd) {
        ClassEnd = classEnd;
    }

    public String getClassRemark() {
        return ClassRemark;
    }

    public void setClassRemark(String classRemark) {
        ClassRemark = classRemark;
    }

    public String getClassUsingId() {
        return ClassUsingId;
    }

    public void setClassUsingId(String classUsingId) {
        ClassUsingId = classUsingId;
    }

    private String ClassBuild;
    private String id;
    private String ClassUsing;
    private String ClassStart;
    private String ClassEnd;
    private String ClassRemark;
    private String ClassUsingId;



}
